class Inventory:
    def __init__(self):
        self.items = {}

    def add_item(self, item_name, quantity):
        if item_name in self.items:
            self.items[item_name] += quantity
        else:
            self.items[item_name] = quantity

    def update_quantity(self, item_name, new_quantity):
        if item_name in self.items:
            self.items[item_name] = new_quantity
        else:
            print(f"Item '{item_name}' not found in inventory.")

    def generate_report(self):
        print("Inventory Report:")
        print("=================")
        for item, quantity in self.items.items():
            print(f"{item}: {quantity} units")

# Sample usage of the Inventory class
def main():
    inventory = Inventory()

    # Adding items to the inventory
    inventory.add_item("Laptop", 10)
    inventory.add_item("Printer", 5)
    inventory.add_item("Mouse", 20)

    # Displaying the initial report
    inventory.generate_report()

    # Updating quantities
    inventory.update_quantity("Laptop", 8)
    inventory.update_quantity("Printer", 3)
    inventory.update_quantity("Keyboard", 5)

    # Displaying the updated report
    inventory.generate_report()

if __name__ == "__main__":
    main()


